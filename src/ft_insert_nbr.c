#include "../includes/libft.h"

char	*ft_insert_nbr(char *arr, int n, int size)
{
	int		i;

	i = size;
	arr[0] = '0';
	if (n == 0)
		return (arr);
	if (n == -2147483648)
	{
		arr[0] = '-';
		arr[1] = '2';
		n = 147483648;
	}
	else if (n < 0)
	{
		arr[0] = '-';
		n = n * (-1);
	}
	else
		i = size - 1;
	while (n != 0)
	{
		arr[i--] = (n % 10) + '0';
		n = n / 10;
	}
	return (arr);
}
