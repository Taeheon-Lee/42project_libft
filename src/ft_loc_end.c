#include "../includes/libft.h"

int	ft_loc_end(char const *s, int len)
{
	len = len - 1;
	while (len >= 0)
	{
		if (s[len] != '\n' && s[len] != '\t' && s[len] != ' ')
			break ;
		len--;
	}
	return (len);
}
