#include "../includes/libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2 || !n)
		return (1);
	while (*s1 && *s2 && !(*s1 - *s2) && n > 1)
	{
		s1++;
		s2++;
		n--;
	}
	if (*s1 - *s2 != 0)
		return (0);
	return (1);
}
